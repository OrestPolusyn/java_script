const formLaptop = document.querySelector('.js-form');
const list = document.querySelector('.laptops-list');
const source = document.querySelector('#laptops-card').innerHTML.trim();
const template = Handlebars.compile(source);
const resetBtn = document.querySelector('button[type=reset]');
let markup = template(laptops);


formLaptop.addEventListener('submit', filtredListLaptop);
resetBtn.addEventListener('click', resetList);
list.insertAdjacentHTML('afterbegin', markup);

function filtredListLaptop(event) {
  event.preventDefault();
  const filter = getArrayCheckedInputs().reduce((acc, elem) => {
    acc[elem.name]
      ? acc[elem.name].push(elem.value)
      : (acc[elem.name] = [elem.value]);
    return acc;
  }, {});
  markup = template(filterCheck(filter, laptops));
  list.innerHTML = markup;
}

function getArrayCheckedInputs() {
  return Array.from(formLaptop.querySelectorAll('input[type="checkbox"]:checked')
  );
}

function filterCheck(arr, laptops) {
  const resultFilter = Object.keys(arr).reduce(
    (a, e) => a.filter(elem => arr[e].includes("" + elem[e])),
    laptops
  );
  return resultFilter;
}

function resetList() {
  let markup = template(laptops);
  return list.innerHTML = markup;
}
